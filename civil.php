<h2>FACILITIES</h2> 
<p>The department is well equipped with various laboratories like Mechanics of Solid, Surveying, Fluid Mechanics, Geology and Concrete Technology with the latest instruments where students are trained for acquiring the in-depth and practical knowledge about various tests.
</p>
<h2>Mechanics of Solid Laboratory</h2>
<p>The laboratory has setup to determine the various properties of different material and carry out experiments for different types of action on force. The laboratory is equipped with universal testing machine, izod impact testing machine, simple lifting machine, etc. 

</p>

<h2>Surveying Laboratory</h2>
<p>The laboratory is well equipped with surveying instruments like metric chain, prismatic compass, dumpy level, transit theodolite, plane table, auto level, planimeter and also the most advanced instruments like total station and global positioning system.

</p>

<h2>Fluid Mechanics Laboratory</h2>
<p>The laboratory is well equipped with various instruments like venturimeter apparatus, metacentric apparatus, bernoulli's theorem apparatus, etc., to carry out expirements related to fluid properties and measurements of different types of flow.
</p>

<h2>Geology Laboratory</h2>
<p>The laboratory has samples of different types of rocks and minerals and also various geological charts, thus helping students in studying various properties of rocks and minerals. 
</p>
<h2>Concrete Technology Laboratory</h2>
<p>The laboratory is well equipped with concrete mixer machine, slump test apparatus, compaction factor apparatus, flow table, vibrating table, vicat needle apparatus, le-chatelier mould, sieve shaker, etc. The lab focuses on training the students for various practicals related to properties of concrete and its material testing.
</p>

<h2>SITE VISITS/ PROJECT WORK</h2>


<p>Construction site visits - Students are taken to on-going construction project sites where they are exposed to various construction activities. Also students are taken to fields where they can carry out different projects related to their syllabus.
<br/>
Industrial site visits- Students are taken to industries that are related to construction material where they are exposed to its manufacturing and testing processes. 

</p>


             

